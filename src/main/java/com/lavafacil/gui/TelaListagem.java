    private void btnAdicionarLavagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarLavagemActionPerformed
        if (tabelaCarro.getSelectedRow() >= 0) {
           if (JOptionPane.showConfirmDialog(null, "Adicionar lavagem?") == 0) {
            int id = Integer.parseInt(tabelaCarro.getValueAt(tabelaCarro.getSelectedRow(), 0).toString());
            CarrosBD CBD = new CarrosBD();
            TelaCadastroLavagem TCL = new TelaCadastroLavagem(CBD.buscarCarroPorId(id));
            TCL.setVisible(true);
            dispose(); }
           }
        else{ JOptionPane.showMessageDialog(rootPane, "Selecione um carro!"); }
    }//GEN-LAST:event_btnAdicionarLavagemActionPerformed

    private void btnExcluirCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirCarroActionPerformed
       if (tabelaCarro.getSelectedRow() >= 0) {
            CarrosBD CBD = new CarrosBD();
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o carro de " + tabelaCarro.getValueAt(tabelaCarro.getSelectedRow(), 2) + "?") == 0) {
           CBD.excluirCarro(CBD.buscarCarroPorId(Integer.parseInt(tabelaCarro.getValueAt(tabelaCarro.getSelectedRow(), 0).toString()))); 
            preencheTabela(CBD.listar()); }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um carro!");
        }
    }//GEN-LAST:event_btnExcluirCarroActionPerformed

