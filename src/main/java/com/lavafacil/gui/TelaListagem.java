public void preencheTabela(List<Carros> carros){ 
    
    if (TelaLogin.conf == 2 ) {
    String columns[] = {"Id", "Placa", "Cliente", "Telefone", "Carro", "Lavagens (todas)", "Completas", "Grátis", "Tempo Limite", "Observações"};
    String dados[][] = new String[carros.size()][columns.length];
    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");  

    int i = 0;
    for (Carros c : carros) {
        Timestamp dataRegistro = c.getTempoLimite();
        String dataFormatada = "";

        if (dataRegistro != null) {
            Timestamp t = Timestamp.valueOf("1970-01-01 00:00:00");
            if (!dataRegistro.equals(t)) {
            LocalDateTime localDateTime = dataRegistro.toLocalDateTime(); 
            dataFormatada = localDateTime.format(formatter); } 
            else { dataFormatada = "N/A"; }
        }

        dados[i] = new String[]{ 
            String.valueOf(c.getId()), 
            c.getPlaca(),
            c.getNomeCliente(),
            c.getTelefone(),
            c.getMarcaCarro(),
            String.valueOf(c.getLavagens()),
            String.valueOf(c.getVisitas()),
            String.valueOf(c.getGratis()),
            dataFormatada,
            c.getObservacoes()
        };              
        i++;
    }

    DefaultTableModel model = new DefaultTableModel(dados, columns) {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false;  // Não permite edição direta das outras células
        }
    };
    tabelaCarro.setModel(model);
 }    
    else {      
        String columns[] = {"Id", "Placa", "Cliente", "Telefone", "Carro", "Lavagens (todas)", "Completas", "Grátis", "Tempo Limite", "Observações"};
        String dados[][] = new String[carros.size()][columns.length];
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");  
    
      int i=0;
      for(Carros c: carros){
        Timestamp dataRegistro = c.getTempoLimite();
        String dataFormatada = "", telefone;
        
        if (TelaInicial.telefone) { telefone = c.getTelefone();} else { telefone = "xx xxxxx-xxxx";}
        
        if (dataRegistro != null) {
            Timestamp t = Timestamp.valueOf("1970-01-01 00:00:00");
            if (!dataRegistro.equals(t)) {
            LocalDateTime localDateTime = dataRegistro.toLocalDateTime(); 
            dataFormatada = localDateTime.format(formatter); } 
            else { dataFormatada = "N/A"; }
        }
          dados[i] = new String[]{ 
              String.valueOf(c.getId()), 
              c.getPlaca(),
              c.getNomeCliente(),
              telefone,
              c.getMarcaCarro(),
              String.valueOf(c.getLavagens()),
              String.valueOf(c.getVisitas()),
              String.valueOf(c.getGratis()),
              dataFormatada,
              c.getObservacoes()};              
          i++;
      }
      
      DefaultTableModel model = new DefaultTableModel(dados, columns){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
      tabelaCarro.setModel(model);  } 
 }
