public static void temaEscuro(JFrame frame, Container container) {
    // Configura o fundo do JFrame e JPanel
    Color fundoEscuro = new Color(46, 46, 46);
    Color fundoClaro = new Color(200, 200, 200);  // Cinza claro para os outros componentes
    Color fonteEscura = Color.BLACK;
    Color fonteClara = Color.WHITE;
    boolean modoEscuro = Boolean.parseBoolean(tema);
    
    if (frame != null) { 
        frame.getContentPane().setBackground(modoEscuro ? fundoEscuro : Color.WHITE);
    }

    for (Component c : container.getComponents()) {
        if (c instanceof JPanel) {
            // Alterar o fundo dos JPanels
            JPanel panel = (JPanel) c;
            panel.setBackground(modoEscuro ? fundoEscuro : Color.WHITE);
        } else if (c instanceof JLabel) { 
            // Alterar a cor dos JLabels
            JLabel label = (JLabel) c;
            if (!"ignorar".equals(label.getName())) {
            label.setForeground(modoEscuro ? fonteClara : fonteEscura); }
        } else if (c instanceof JButton) {
            // Alterar a cor dos JButtons
            JButton button = (JButton) c;
            if (!"ignorar".equals(button.getName())) {
            button.setBackground(modoEscuro ? fundoClaro : null); // Define o fundo como cinza claro ou o padrão
            }
        } else if (c instanceof JTextField) {
            // Alterar a cor dos JTextFields
            JTextField textField = (JTextField) c;
            textField.setBackground(modoEscuro ? fundoClaro : Color.WHITE);
        } else if (c instanceof JTextArea) {
            // Alterar a cor dos JTextFields
            JTextArea textArea = (JTextArea) c;
            if (!"ignorar".equals(textArea.getName())) {
            textArea.setBackground(modoEscuro ? fundoClaro : Color.WHITE); }
        } else if (c instanceof JComboBox) {
            // Alterar a cor dos JComboBox
            JComboBox comboBox = (JComboBox) c;
            comboBox.setBackground(modoEscuro ? fundoClaro : Color.WHITE);
        } else if (c instanceof JCheckBox) {
            // Alterar a cor dos JComboBox
            JCheckBox checkBox = (JCheckBox) c;
            checkBox.setBackground(modoEscuro ? fundoEscuro : Color.WHITE);
            checkBox.setForeground(modoEscuro ? fonteClara : fonteEscura);
        } else if (c instanceof JTable) {
            JTable table = (JTable) c;
            
            // Alterar o fundo e o texto da tabela
            table.setBackground(modoEscuro ? fundoEscuro : Color.WHITE);
            table.setForeground(modoEscuro ? fonteClara : fonteEscura);

            // Alterar a cor de seleção da tabela
            table.setSelectionBackground(modoEscuro ? new Color(70, 70, 70) : new Color(184, 207, 229));
            table.setSelectionForeground(modoEscuro ? fonteClara : fonteEscura);

            // Alterar a cor do cabeçalho da tabela
            JTableHeader header = table.getTableHeader();
            header.setBackground(modoEscuro ? fundoClaro : Color.LIGHT_GRAY);
            header.setForeground(modoEscuro ? fonteClara : fonteEscura);
            
            // Alterar fundo da tabela
            if (table.getParent() instanceof JViewport) {
            table.getParent().setBackground(modoEscuro ? fundoEscuro : Color.WHITE); }
            
            // (Opcional) Redesenhar a tabela para garantir que as mudanças sejam aplicadas
            table.repaint();
        } else if (c instanceof JRadioButton) {
            // Alterar a cor das JTables
            JRadioButton button = (JRadioButton) c;
            button.setBackground(modoEscuro ? fundoEscuro : Color.WHITE);
            button.setForeground(modoEscuro ? fonteClara : fonteEscura);
        }

        // Se houver outros containers aninhados, iterar neles também
        if (c instanceof Container) {
            temaEscuro(null, (Container) c);
        }
    }
 }
