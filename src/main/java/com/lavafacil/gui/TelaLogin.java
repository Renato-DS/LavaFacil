    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Usuario usuario = new Usuario();
        UsuarioBD u = new UsuarioBD();
        try{
            char[] passwordChars = txtSenhaSec.getPassword();
            String password = new String(passwordChars);
            usuario = u.validarUsuario(txtLogin.getText(), password);
            if (usuario != null) {

                JOptionPane.showMessageDialog(null, "Olá " + usuario.getNome() +
                    ", sua permissão é " + usuario.getTipo() + "(a). Seja bem-vindo!");

                TelaInicial TI = new TelaInicial(usuario);

                switch (usuario.getTipo()) {
                    case "Usuário": conf = 0; l = usuario.getLogin(); s = usuario.getSenha(); TI.setVisible(true);  dispose(); break;
                    case "Operador": conf = 1; l = usuario.getLogin(); s = usuario.getSenha(); TI.setVisible(true); dispose(); break;
                    case "Administrador": conf = 2; l = usuario.getLogin(); s = usuario.getSenha(); TI.setVisible(true);  dispose();  break;
                }   }
                else { JOptionPane.showMessageDialog(null, "Nenhum usuário encontrado! Verifique se os dados estão corretos."); }
            }catch(Exception e){JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());}
    }//GEN-LAST:event_btnLoginActionPerformed

